#include "libft.h"

void	ft_rmvspc(char **s)
{
	int	i;
	int	j;
	int	size;

	size = ft_strlen(*s);
	i = 0;
	while (i < size)
	{	
		if ((*s)[i] == ' ')
		{
			j = i;
			while (j < size)
			{
				(*s)[j] = (*s)[j + 1];
				j++;
			}
		}
		if ((*s)[i] != ' ')
			i++;
	}
}
