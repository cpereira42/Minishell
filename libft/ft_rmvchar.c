#include "libft.h"

void	ft_rmvchar(char **s, char c)
{
	int	i;
	int	j;
	int	size;

	size = ft_strlen(*s);
	i = 0;
	while (i < size)
	{	
		if ((*s)[i] == c)
		{
			j = i;
			while (j < size)
			{
				(*s)[j] = (*s)[j + 1];
				j++;
			}
		}
		if ((*s)[i] != c)
			i++;
	}
}
