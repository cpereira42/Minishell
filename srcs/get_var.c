#include "minishell.h"

char	*get_var(t_v *v)
{
	char	*var;
	int		i;

	if (v->cmd.cmd_args[1] != 0)
	{
		var = ft_strdup(v->cmd.cmd_args[1]);
		i = 0;
		while (var[i] != 0 && var[i] != '=')
			i++;
		var[i] = 0;
	}
	else
		var = ft_strdup("");
	return (var);
}
