#include "minishell.h"

int	free_matrizes(char **s)
{
	int i;
	int n;

	i = 0;
	n = ft_conta_linhas(s);
	while (i < n)
	{
		free(s[i]);
		i++;
	}
	free(s);
	return (0);
}
