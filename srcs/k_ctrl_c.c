#include "minishell.h"

void	k_ctrl_c(t_v *v)
{
	ft_bzero(v->ret2, 2048);
	ft_bzero(v->ret, 2048);
	ft_putstr_fd("\n", 1);
	ft_putstr_fd(v->prompt,1);
	tputs(save_cursor,1,my_termprint);
}
